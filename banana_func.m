function [f, grad] = banana_func(x)
	%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
	% This function evaluates the banana function and its gradient.
	%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
	%%%%% Inputs:
	% x:    2 x 1
	%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
	%%%%% Outputs:
	% f:    scalar
	% grad: 2 x 1
	%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
	
	x1 = x(1);
	x2 = x(2);
	
	f = 100*(x2-x1^2)^2 + (1-x1)^2;
	
	grad = [0;0];
	grad(1) = -400*x1*(x2-x1^2) - 2*(1-x1);
	grad(2) = 200*(x2-x1^2);
end
